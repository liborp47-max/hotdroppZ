create policy "auth_all_generated_articles"  on generated_articles  for all to authenticated using (true) with check (true);
create policy "auth_all_distribution_queue"  on distribution_queue  for all to authenticated using (true) with check (true);
create policy "auth_all_audit_runs"          on audit_runs          for all to authenticated using (true) with check (true);
create policy "auth_all_error_logs"          on error_logs          for all to authenticated using (true) with check (true);
